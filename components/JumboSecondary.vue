<template>
  <div
    class="hero bg-cover bg-opacity-60"
    :style="{ backgroundImage: bgImageResolved, minHeight }"
  >
    <div class="hero-overlay"></div>
    <div class="hero-overlay"></div>
    <div class="hero-content text-neutral-content text-center relative z-[1]">
      <div class="max-w-md">
        <h1 class="my-10 text-5xl font-bold">{{ title }}</h1>
        <p class="mb-5 text-lg">{{ subtitle }}</p>
        <slot />
      </div>
    </div>
  </div>
</template>

<script setup lang="ts">
const props = withDefaults(
  defineProps<{
    title: string
    subtitle: string
    minHeight?: string
    bgImage?: string
  }>(),
  {
    minHeight: '50vh',
    bgImage: '/images/bcn.jpg',
    title: '',
    subtitle: '',
  },
)

const bgImageResolved = computed(() => {
  const imgUrl = props.bgImage
  return `url('${imgUrl}')`
})
</script>
